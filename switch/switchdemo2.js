var season = "rainy";

switch (season) {

    case "spring":
        console.log("It's spring! The trees are budding!");
        break;
    case "summer":
        console.log("It's summer! Time to hit the beach!");
        break;
    case "fall":
        console.log("It's fall! Time to hit the books!");
        break;
    case "winter":
        console.log("It's winter! Time to hit the slopes!");
        break;
    case "rainy":
        console.log("It's rainy! Time to hit the umbrella!");
        break;
    default:
        console.log("Invalid season");
        break;                 
}